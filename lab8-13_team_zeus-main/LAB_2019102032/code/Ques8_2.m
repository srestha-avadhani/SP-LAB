N =10;
x1 = randn(1,N);
x2 = randn(1,N);
len = max(length(x1),length(x2));
[c1,c2] = dftconv(x1,x2);
d1 = cconv(x1,x2,len);
d2 = conv(x1,x2);
figure;
subplot(2,2,1);
stem(c1);
title('circular');
subplot(2,2,2);
stem(d1);
title('circular');
subplot(2,2,3);
stem(c2);
title('linear');
subplot(2,2,4);
stem(d2);
title('linear');